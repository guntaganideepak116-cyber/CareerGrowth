// Helper function to get projects by field ID
export function getProjectsByField(fieldId: string): FieldProject[] {
  return projectsMap[fieldId] || [];
}

// Get all unique skills across all projects
export function getAllProjectSkills(): string[] {
  const skills = new Set<string>();
  Object.values(projectsMap).forEach(projects => {
    projects.forEach(project => {
      project.skills.forEach(skill => skills.add(skill));
    });
  });
  return Array.from(skills).sort();
}
