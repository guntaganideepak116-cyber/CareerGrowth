// ============================================
// GENERATE ROADMAP FUNCTION
// ============================================

export function generateRoadmap(fieldId: string, specializationId: string): RoadmapPhase[] {
  // First, check if we have a specific roadmap for this field+specialization combo
  if (roadmapTemplates[fieldId]?.[specializationId]) {
    return roadmapTemplates[fieldId][specializationId];
  }

  // Branch-specific logic (Engineering)
  if (specializationId.startsWith('cse-')) return cseBasePhases;
  if (specializationId.startsWith('ece-')) return eceBasePhases;
  if (specializationId.startsWith('eee-')) return eeeBasePhases;
  if (specializationId.startsWith('mech-')) return mechBasePhases;
  if (specializationId.startsWith('civil-')) return civilBasePhases;

  // Return the field-specific base roadmap
  const basePhases = fieldBasePhases[fieldId];

  if (basePhases) {
    // Customize the base phases with specialization context
    return basePhases.map(phase => ({
      ...phase,
      careerRelevance: `${phase.careerRelevance} - tailored for ${specializationId.replace(/-/g, ' ')} specialization`,
    }));
  }

  // Ultimate fallback for unknown fields (should rarely happen)
  return engineeringBasePhases.map(phase => ({
    ...phase,
    careerRelevance: `${phase.careerRelevance} - adapted for your career path`,
  }));
}
